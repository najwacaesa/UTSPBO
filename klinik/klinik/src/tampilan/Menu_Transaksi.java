    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        java.sql.Date df = new java.sql.Date(txtTanggal.getDate(). getTime());
        String ID = this.txtIDtrans.getText().trim();
        String NamaPasien = this.txtNamapasien.getText().trim();
        String IDDokter = this.txtIDdokter.getText().trim();
        String Layanan = this.txtLayanan.getText().trim();
        String Total = this.txtTotal.getText().trim();


        if (ID.isEmpty() || NamaPasien.isEmpty() || IDDokter.isEmpty() || Layanan.isEmpty() || Total.isEmpty()) { 
            JOptionPane.showMessageDialog(this, "Data Tidak Boleh Kosong");
        } else { 
            Transaksi transaksi = new Transaksi();
            transaksi.ID = ID;
            transaksi.namapasien = NamaPasien;
            transaksi.tanggal = df;
            transaksi.IDdokter = IDDokter;
            transaksi.layanan = Layanan;
            transaksi.total = Total;

            if (transaksi.create()) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
                tampilkan();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan");
            }
        }
    }           
    
    private void tampilkan() {
        Database konek = new Database();
        
        konek.openConnection();
        
        ArrayList<ArrayList> user = konek.all("SELECT * FROM transaksi");
       
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama Pasien"); 
        model.addColumn("Tanggal");
        model.addColumn("ID Dokter");
        model.addColumn("Layanan");
        model.addColumn("Total");
        
        
        for (ArrayList<String> rowData : user) {
            model.addRow(rowData.toArray());
        }
        
        konek.closeConnection();
        tabel_transaksi.setModel(model);                                     
    }//GEN-LAST:event_SaveActionPerformed

