    private void tabel_transaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_transaksiMouseClicked
        // TODO add your handling code here:
        int row = tabel_transaksi.rowAtPoint(evt.getPoint());
        String ID = tabel_transaksi.getValueAt(row, 0).toString();
        txtIDtrans.setText(ID);

        String namapasien = tabel_transaksi.getValueAt(row, 1).toString();
        txtNamapasien.setText(namapasien);

        String dateStr = tabel_transaksi.getValueAt(row, 2).toString();
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd"); //
        Date date = null;
        try {
            date = df.parse(dateStr);
        } catch (ParseException ex) {
            Logger.getLogger(Dokter.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtTanggal.setDate(date);
        
        
        String IDDokter = tabel_transaksi.getValueAt(row,3).toString();
        txtIDdokter.setText(IDDokter);
        
        String Layanan = tabel_transaksi.getValueAt(row,4).toString();
        txtLayanan.setText(Layanan);

        String Total = tabel_transaksi.getValueAt(row, 5).toString();
        txtTotal.setText(Total);
    }//GEN-LAST:event_tabel_transaksiMouseClicked

