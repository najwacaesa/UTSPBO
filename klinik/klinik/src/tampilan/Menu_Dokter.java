    private void tabel_dokterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_dokterMouseClicked
        // TODO add your handling code here:
        int row = tabel_dokter.rowAtPoint(evt.getPoint());
        String ID = tabel_dokter.getValueAt(row, 0).toString();
        txtID.setText(ID);

        String nama = tabel_dokter.getValueAt(row, 1).toString();
        txtNama.setText(nama);

        String Spesialisasi = tabel_dokter.getValueAt(row,2).toString();
        txtSpesialis.setText(Spesialisasi);

        String dateStr = tabel_dokter.getValueAt(row, 3).toString();
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd"); //
        Date date = null;
        try {
            date = df.parse(dateStr);
        } catch (ParseException ex) {
            Logger.getLogger(Dokter.class.getName()).log(Level.SEVERE, null, ex);
        }
        tanggal.setDate(date);
        
        String nohp = tabel_dokter.getValueAt(row, 4).toString();
        txtNOHP.setText(nohp);
        
        String email = tabel_dokter.getValueAt(row, 5).toString();
        txtemail.setText(email);
    }//GEN-LAST:event_tabel_dokterMouseClicked

