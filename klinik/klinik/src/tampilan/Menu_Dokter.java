    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        java.sql.Date df = new java.sql.Date(tanggal.getDate(). getTime());
        String ID = this.txtID.getText().trim();
        String Nama = this.txtNama.getText().trim();
        String Spesialisasi = this.txtSpesialis.getText().trim();
        String noHP = this.txtNOHP.getText().trim();
        String email = this.txtemail.getText().trim();


        if (ID.isEmpty() || Nama.isEmpty() || Spesialisasi.isEmpty() || noHP.isEmpty() || email.isEmpty()) { 
            JOptionPane.showMessageDialog(this, "Data Tidak Boleh Kosong");
        } else { 
            Dokter dokter = new Dokter();
            dokter.ID = ID;
            dokter.nama = Nama;
            dokter.spesialisasi = Spesialisasi;
            dokter.tanggallahir = df;
            dokter.nohp = noHP;
            dokter.email = email;

            if (dokter.create()) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
                tampilkan();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan");
            }
        }
    }           
    
    private void tampilkan() {
        Database konek = new Database();
        
        konek.openConnection();
        
        ArrayList<ArrayList> user = konek.all("SELECT * FROM dokter");
       
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Spesialisasi");
        model.addColumn("Tanggal Lahir");
        model.addColumn("No HP");
        model.addColumn("Email");
        
        
        for (ArrayList<String> rowData : user) {
            model.addRow(rowData.toArray());
        }
        
        konek.closeConnection();
        tabel_dokter.setModel(model); 
    }//GEN-LAST:event_SaveActionPerformed

