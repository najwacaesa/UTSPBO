    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        // TODO add your handling code here
        java.sql.Date df = new java.sql.Date(txtTanggal.getDate(). getTime());
        String ID = this.txtID.getText().trim();
        String Namapasien = this.txtNamapasien.getText().trim();
        String IDDokter = this.txtIDDokter.getText().trim();
        String Layanan = this.txtLayanan.getText().trim();
        String Status = (String) this.txtstatus.getSelectedItem();


        if (ID.isEmpty() || Namapasien.isEmpty() || IDDokter.isEmpty() || Layanan.isEmpty()) { 
            JOptionPane.showMessageDialog(this, "Data Tidak Boleh Kosong");
        } else { 
            Bookingpasien booking = new Bookingpasien();
            booking.ID = ID ;
            booking.tanggal = df;
            booking.namapasien = Namapasien;
            booking.IDdokter = IDDokter;
            booking.layanan = Layanan;
            booking.status = Status;
;
            if (booking.update()) {
                JOptionPane.showMessageDialog(this, "Data berhasil diedit");
                tampilkan();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan");
            }    
          }
    }//GEN-LAST:event_EditActionPerformed

    private void tabel_bookingMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_bookingMouseClicked
        // TODO add your handling code here:
        int row = tabel_booking.rowAtPoint(evt.getPoint());
        String ID = tabel_booking.getValueAt(row, 0).toString();
        txtID.setText(ID);

        String dateStr = tabel_booking.getValueAt(row, 1).toString();
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd"); //
        Date date = null;
        try {
            date = df.parse(dateStr);
        } catch (ParseException ex) {
            Logger.getLogger(Dokter.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtTanggal.setDate(date);
        
        String namapasien = tabel_booking.getValueAt(row, 2).toString();
        txtNamapasien.setText(namapasien);
        
        String IDDokter = tabel_booking.getValueAt(row,3).toString();
        txtIDDokter.setText(IDDokter);
        
        String Layanan = tabel_booking.getValueAt(row,4).toString();
        txtLayanan.setText(Layanan);
        
        String Status = tabel_booking.getValueAt(row,4).toString();
        txtstatus.setSelectedItem(Status);
    }//GEN-LAST:event_tabel_bookingMouseClicked

